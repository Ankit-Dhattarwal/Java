public class Java_ch02 {
    public static void main(String[] args) {
        int a = 5;
        a += 5;
        a *= 5;     // This is used for adding 5 in b which already have 5 so the result is 10;
        System.out.println(a);
        System.out.println(64<6);    //   comparison Operators // In this we simple compare the values equals or not.
        System.out.println(64>6 && 64>9); // logical Operators
        System.out.println(2&3);  // Bitwise Operators
    }
}
